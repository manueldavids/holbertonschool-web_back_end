#!/usr/bin/env python3
"""
Module for basic addition function with type annotations.
This module provides a type-annotated function to add two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return their sum.
    
    Args:
        a (float): The first number to add
        b (float): The second number to add
        
    Returns:
        float: The sum of a and b
    """
    return a + b 