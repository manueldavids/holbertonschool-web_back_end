#!/usr/bin/env python3
"""
Module for string concatenation function with type annotations.
This module provides a type-annotated function to concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings and return the result.

    Args:
        str1 (str): The first string to concatenate
        str2 (str): The second string to concatenate

    Returns:
        str: The concatenated string
    """
    return str1 + str2
