#!/usr/bin/env python3
"""
Module for float to string conversion function with type annotations.
This module provides a type-annotated function to convert float to string.
"""


def to_str(n: float) -> str:
    """
    Convert a float number to its string representation.

    Args:
        n (float): The float number to convert to string

    Returns:
        str: The string representation of the float
    """
    return str(n) 